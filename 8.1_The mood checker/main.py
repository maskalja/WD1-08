mood = input("Hey, what is you mood? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up!")
elif mood == "excited":
    print("Do not get too excited:)!")
elif mood == "relaxed":
    print("Yeah buddy!")
else:
    print("You're just typing in nebuloze instead of recognized mood statuses!")